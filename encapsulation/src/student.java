public class student {

    //private data members
    private String name;
    private int roll;
    private int age;

    //getter method for student name to access private variable
    public String getName() {
        return name;
    }

    //setter method for student name to set the value in private variable
    public void setName(String name) {
        this.name = name;
    }

    //getter method for student roll to access private variable
    public int getRoll() {
        return roll;
    }

    //setter method for student roll to set the value in private variable
    public void setRoll(int roll) {
        this.roll = roll;
    }

    //getter method for student age to access private variable
    public int getAge() {
        return age;
    }

    //setter method for student age to set the value in private variable
    public void setAge(int age) {
        this.age = age;
    }
}
