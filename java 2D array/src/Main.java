public class Main {
    public static void main(String[] args) {
        int arr[][] = {{2,5}, {4,0}, {9,1}};
        for (int i = 0 ; i < 3 ; i++) {
            for (int j = 0 ; j < 3 ; j++) {
                System.out.println("arr[" + i + "][" + j + "] = " + arr[i][j] + " ");
            }
        }
    }
}